# Password hashing
def hash_password(password):
    return hashlib.sha256(password.encode()).hexdigest()

# Authentication functions
def login_user(username, password, collection):
    """Login user and return success status"""
    user = collection.find_one({'username': username})
    if user and user['password'] == hash_password(password):
        # Update last login
        collection.update_one(
            {'username': username},
            {'$set': {'last_login': datetime.now()}}
        )
        return True, user
    return False, None

def register_user(username, password, email, collection):
    """Register new user"""
    # Check if username exists
    if collection.find_one({'username': username}):
        return False, 'å¸³è™Ÿå·²å­˜åœ¨'

    # Check if email exists
    if email and collection.find_one({'email': email}):
        return False, 'Email å·²è¢«ä½¿ç”¨'

    # Create new user
    new_user = {
        'username': username,
        'password': hash_password(password),
        'email': email,
        'created_at': datetime.now(),
        'last_login': None
    }

    collection.insert_one(new_user)
    return True, 'è¨»å†ŠæˆåŠŸï¼'

